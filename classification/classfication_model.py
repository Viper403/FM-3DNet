class Mclassification(nn.Module):
    def __init__(self, args,output_channels=40):
        super(Mclassification, self).__init__()
        self.args = args
        self.linear_1 = nn.Linear(args.emb_dims*2, 512, bias=False)
        self.bn_6 = nn.BatchNorm1d(512)
        self.dp_1 = nn.Dropout(p=args.dropout)
        self.linear_2 = nn.Linear(512, 256)
        self.bn_7 = nn.BatchNorm1d(256)
        self.dp_2 = nn.Dropout(p=args.dropout)
        self.linear_3 = nn.Linear(256, output_channels)


    def forward(self,x):
        #print(x.size())
        batch_size = x.size(0)
        x1 = F.adaptive_max_pool1d(x, 1).view(batch_size, -1)
        x2 = F.adaptive_avg_pool1d(x, 1).view(batch_size, -1)
        x = torch.cat((x1, x2), 1)

        x = F.leaky_relu(self.bn_6(self.linear_1(x)), negative_slope=0.2)
        x = self.dp_1(x)
        x = F.leaky_relu(self.bn_7(self.linear_2(x)), negative_slope=0.2)
        x = self.dp_2(x)
        x = self.linear_3(x)

        return x

